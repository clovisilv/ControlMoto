    public void atualizaGrid(){
        CadTarefaVO cadTarefaVO = null; CadTarefaDAO cadTarefaDAO = null;
        List<CadTarefaVO> epdList = null;
        DefaultTableModel modelo; DefaultTableCellRenderer esquerda=null, direita=null, centro=null;
        try {
            esquerda = new DefaultTableCellRenderer(); direita = new DefaultTableCellRenderer(); centro = new DefaultTableCellRenderer();

            esquerda.setHorizontalAlignment(SwingConstants.LEFT); direita.setHorizontalAlignment(SwingConstants.RIGHT);
            centro.setHorizontalAlignment(SwingConstants.CENTER);
            
            epdList = new ArrayList<CadTarefaVO>();
            
            cadTarefaVO = new CadTarefaVO(); cadTarefaDAO = new CadTarefaDAO();
            
            epdList = cadTarefaDAO.listaTarefas(cadTarefaVO);
            
            modelo = (DefaultTableModel) this.jTable1.getModel();
            modelo.setNumRows(0);
            for(CadTarefaVO cTarefaVO : epdList){
               modelo.addRow(new String[]{ String.valueOf(cTarefaVO.getCodigoTarefa()), cTarefaVO.getDescricaoTarefa(), cTarefaVO.getStatusTarefa() });
            }
            
            jTable1.getColumnModel().getColumn(0).setCellRenderer(direita);
            jTable1.getColumnModel().getColumn(1).setCellRenderer(esquerda);
            jTable1.getColumnModel().getColumn(2).setCellRenderer(esquerda);
            //jTable1.getColumnModel().getColumn(3).setCellRenderer(centro);
            
        } catch (SQLException | ClassNotFoundException | IllegalAccessException | InstantiationException ex) {
            Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                cadTarefaDAO.closeTarefaConexao();
            } catch ( SQLException | ClassNotFoundException ex) {
                Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
