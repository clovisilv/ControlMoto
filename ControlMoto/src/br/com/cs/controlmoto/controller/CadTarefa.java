    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        CadTarefaVO cadTarefaVO = null; CadTarefaDAO cadTarefaDAO=null;
        try {
            int linha = jTable1.getSelectedRow();
            
            cadTarefaVO = new CadTarefaVO(); cadTarefaDAO = new CadTarefaDAO();
            
            cadTarefaVO.setCodigoTarefa(Integer.valueOf(jTable1.getValueAt(linha, 0).toString()));

            cadTarefaVO = cadTarefaDAO.buscaTarefaByPk(cadTarefaVO);
            
            lblVlCodTarefa.setText(String.valueOf(cadTarefaVO.getCodigoTarefa()));
            txtDescricaoTarefa.setText(cadTarefaVO.getDescricaoTarefa());
            if("Sim".equals(cadTarefaVO.getStatusTarefa())){
                jrbAtivo.setSelected(true);
            }else{
                jrbAtivo.setSelected(false);            
            }      
        } catch ( SQLException | ClassNotFoundException | IllegalAccessException | InstantiationException ex) {
            Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            try {
                cadTarefaDAO.closeTarefaConexao();
                btoIncluir.setEnabled(false);btoSalvar.setEnabled(false);btoAtualizar.setEnabled(true);
            } catch ( SQLException | ClassNotFoundException ex) {
                Logger.getLogger(ContasPagar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }                                    

