	private void btoIncluirActionPerformed(java.awt.event.ActionEvent evt) {                                           
        CadTarefaVO cadTarefaVO = null; CadTarefaDAO cadTarefaDAO = null;
        try {
            cadTarefaVO = new CadTarefaVO(); cadTarefaDAO = new CadTarefaDAO();
            
            cadTarefaVO = cadTarefaDAO.controlaPkTarefa();
            String id = String.valueOf(cadTarefaVO.getCodigoTarefa());
            if(!id.isEmpty() && id != "null"){
                lblVlCodTarefa.setText(String.valueOf(cadTarefaVO.getCodigoTarefa()+1));
            }else{
                lblVlCodTarefa.setText(String.valueOf(1));
            }
        } catch (SQLException | ClassNotFoundException | IllegalAccessException | InstantiationException ex) {
            Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            try {
                cadTarefaDAO.closeTarefaConexao();
                btoIncluir.setEnabled(false);btoSalvar.setEnabled(true);btoAtualizar.setEnabled(false);
            } catch ( SQLException | ClassNotFoundException ex) {
                Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }                                          

    private void btoSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        CadTarefaVO cadTarefaVO = null; CadTarefaDAO cadTarefaDAO = null;
        try {
            cadTarefaVO = new CadTarefaVO(); cadTarefaDAO = new CadTarefaDAO();
            
            cadTarefaVO.setCodigoTarefa(Integer.valueOf(lblVlCodTarefa.getText()));
            cadTarefaVO.setDescricaoTarefa(txtDescricaoTarefa.getText());
            if(jrbAtivo.isSelected()){
                cadTarefaVO.setStatusTarefa("Sim");
            }else{
                cadTarefaVO.setStatusTarefa("Não");
            }
            cadTarefaDAO.salvaTarefa(cadTarefaVO);
        } catch (SQLException | ClassNotFoundException | IllegalAccessException | InstantiationException ex) {
            Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            try {
                cadTarefaDAO.closeTarefaConexao();
                limpaCampos();
                atualizaGrid();
                btoIncluir.setEnabled(true);btoSalvar.setEnabled(false);btoAtualizar.setEnabled(false);
            } catch ( SQLException | ClassNotFoundException ex) {
                Logger.getLogger(CadTarefa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }                                         

